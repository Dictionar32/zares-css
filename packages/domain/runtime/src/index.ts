// createComponent
// ─────────────────────────────────────────────────────────────────────────────

/**
 * Build a compound styled React component from Rust-generated metadata.
 *
 * @param tag           HTML element tag, e.g. "button" | "div"
 * @param baseClass     Scoped base class from Rust, e.g. "Button_abc123"
 * @param subComponents Map of subcomponent definitions (optional)
 * @param conditionals  Map of prop name → CSS class (optional)
 *
 * @example
 * // Injected automatically by the compiler — do not call manually
 * const Button = createComponent("button", "Button_abc123", {
 *   icon: { tag: "span", class: "Button_icon_abc123" },
 *   text: { tag: "span", class: "Button_text_abc123" },
 * }, { fullWidth: "w-full" })
 *
 * // Usage in JSX:
 * <Button fullWidth>
 *   <Button.icon>🔍</Button.icon>
 *   <Button.text>Search</Button.text>
 * </Button>
 */
export function createComponent<S extends SubComponentMap = SubComponentMap>(
  tag: string,
  baseClass: string,
  subComponents?: S,
  conditionals?: ConditionalProps
): StyledComponent<S> {
  type Props = React.HTMLAttributes<HTMLElement> &
    React.RefAttributes<HTMLElement> &
    Record<string, unknown>

  // ── Base component ─────────────────────────────────────────────────────────
  const Base = React.forwardRef<HTMLElement, Props>(function TwBase(props, ref) {
    const extra: string[] = []

    // Collect conditional classes
    if (conditionals) {
      for (const [prop, cls] of Object.entries(conditionals)) {
        if (props[prop]) extra.push(cls)
      }
    }

    // Build clean props — strip conditional + subcomponent boolean props
    const clean: Record<string, unknown> = {}
    const stripKeys = new Set([
      "className",
      ...(conditionals ? Object.keys(conditionals) : []),
      ...(subComponents ? Object.keys(subComponents) : []),
    ])
    for (const [k, v] of Object.entries(props)) {
      if (!stripKeys.has(k)) clean[k] = v
    }

    const className = cx(baseClass, ...extra, (props.className as string) ?? "")

    return React.createElement(tag, { ...clean, ref, className: className || undefined })
  })

    ; (Base as { displayName?: string }).displayName = `tw.${String(tag)}`

  // ── Attach subcomponent properties ────────────────────────────────────────
  if (subComponents) {
    for (const [subName, def] of Object.entries(subComponents)) {
      const subTag = def.tag ?? "span"
      const subClass = def.class

      const SubComp = React.forwardRef<HTMLElement, Props>(function TwSub(props, ref) {
        const className = cx(subClass, (props.className as string) ?? "")
        const clean: Record<string, unknown> = { ...props }
        delete clean.className
        return React.createElement(subTag, { ...clean, ref, className: className || undefined })
      })

        ; (SubComp as { displayName?: string }).displayName = `tw.${String(tag)}.${String(subName)}`

        ; (Base as unknown as Record<string, unknown>)[subName] = SubComp
    }
  }

  return Base as StyledComponent<S>
}

