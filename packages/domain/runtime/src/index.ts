export interface SubComponentDef {
  /** HTML tag to render (default: "span") */
  tag?: string
  /** Scoped CSS class generated by Rust, e.g. "Button_icon_abc123" */
  class: string
}

export type SubComponentMap = Record<string, SubComponentDef>

/**
 * Map of prop name → CSS class appended when the prop is truthy.
 * e.g. { fullWidth: "w-full", large: "text-lg px-6 py-3" }
 */
export type ConditionalProps = Record<string, string>

/**
 * A compound styled component: base ForwardRef + attached .SubName properties.
 * The generic params are intentionally loose so callers don't need to repeat them.
 */
export type StyledComponent<S extends SubComponentMap = SubComponentMap> =
  React.ForwardRefExoticComponent<
    React.HTMLAttributes<HTMLElement> & React.RefAttributes<HTMLElement>
  > & {
    [K in keyof S]: React.ForwardRefExoticComponent<
      React.HTMLAttributes<HTMLElement> & React.RefAttributes<HTMLElement>
    >
  }

/** Metadata for one compound component produced by Rust. */
export interface ComponentMetadata {
  component: string
  tag: string
  baseClass: string
  subComponents: SubComponentMap
}

// ─────────────────────────────────────────────────────────────────────────────
// cx — tiny className utility (no external dependency)
// ─────────────────────────────────────────────────────────────────────────────

type ClassInput = string | number | false | null | undefined | ClassInput[]

export function cx(...inputs: ClassInput[]): string {
  const out: string[] = []
  for (const input of inputs) {
    if (!input) continue // filters 0, false, null, undefined, ""
    if (typeof input === "string") {
      out.push(input)
    } else if (typeof input === "number") {
      out.push(String(input))
    } else if (Array.isArray(input)) {
      const nested = cx(...input)
      if (nested) out.push(nested)
    }
  }
  return out.join(" ")
}

